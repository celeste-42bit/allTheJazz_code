def mkch2(redA, redB):
    userinput = str(input("ENTER: "))
    while userinput != "A" and userinput != "B" and userinput != "a" and userinput != "b":
        print("Enter either A(a) or B(b)!")
        userinput = str(input("ENTER: "))
    else:
        if userinput == "A" or userinput == "a":
            return "A", redA
        elif userinput == "B" or userinput == "b":
            return "B", redB



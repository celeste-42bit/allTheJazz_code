    while user_input != "A" and user_input != "B" and user_input != "a" and user_input != "b":
        print("Enter either A(a) or B(b)!")
        user_input = str(input("Type your choice: "))
    else:
        if user_input == "A" or user_input == "a":
            return "A", script0
        elif user_input == "B" or user_input == "b":
            return "B", script1


